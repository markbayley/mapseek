// Formatting functions
export function formatProduction(n: number): string {
  if (n >= 1000000) return (n / 1000000).toFixed(1).replace(/\.0$/, '') + 'M t';
  if (n >= 1000) return (n / 1000).toFixed(0) + 'k t';
  return n + ' t';
}
export function formatCoal(n: number): string {
  return n + ' Mt';
}
export function formatGas(n: number): string {
  return (n / 1000).toFixed(0) + ' B mÂ³';
}
export function formatPetroleum(n: number): string {
  return (n / 1000000).toFixed(1).replace(/\.0$/, '') + ' M bbl/d';
}
export function formatCobalt(n: number): string {
  return n + ' t';
}
export function formatDiamond(n: number): string {
  return n + ' M ct';
}
export function formatGold(n: number): string {
  return n + ' t';
}
export function formatAluminium(n: number): string {
  return n + 'k t';
}
export function formatIronOre(n: number): string {
  return n + 'k t';
}
export function formatSilver(n: number): string {
  return n + ' t';
}

